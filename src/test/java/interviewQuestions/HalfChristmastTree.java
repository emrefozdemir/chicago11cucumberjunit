package interviewQuestions;

public class HalfChristmastTree {
    public static void main(String[] args) {

        // to create 5 lines of half Christmast Tree

            for(int i=0; i<5; i++)
            {
                for(int j=0; j<=i; j++)
                {
                    System.out.print("*");
                }
                System.out.println();
            }

    }
}
